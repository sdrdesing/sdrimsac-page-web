    // Funcionalidad de tabs
    document.addEventListener('DOMContentLoaded', function() {
        const tabs = document.querySelectorAll('.equipo-tab');
        const profiles = document.querySelectorAll('.equipo-profile');
        tabs.forEach(tab => {
            tab.addEventListener('click', function() {
                tabs.forEach(t => t.classList.remove('active'));
                tab.classList.add('active');
                const target = tab.getAttribute('data-tab');
                profiles.forEach(profile => {
                    if(profile.getAttribute('data-profile') === target){
                        profile.style.display = '';
                        profile.classList.add('equipo-profile-active');
                    } else {
                        profile.style.display = 'none';
                        profile.classList.remove('equipo-profile-active');
                    }
                });
            });
        });
    });
